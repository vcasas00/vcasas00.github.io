const translate = (key) => translations[currentLanguage]?.[key] ?? translations[defaultLanguage][key] ?? key;


const updateThemeToggleLabel = () => {
  const isLight = currentTheme === "light";
  themeToggle?.setAttribute("aria-label", translate(isLight ? "aria.switchToDark" : "aria.switchToLight"));
  themeToggle?.setAttribute("aria-pressed", String(isLight));
};

const applyTheme = (theme) => {
  currentTheme = theme === "light" ? "light" : "dark";
  document.documentElement.dataset.theme = currentTheme;
  updateThemeToggleLabel();
  saveTheme(currentTheme);
};
const updateNavToggleLabel = () => {
  const isOpen = navLinks?.classList.contains("is-open") ?? false;
  navToggle?.setAttribute("aria-label", translate(isOpen ? "aria.closeMenu" : "aria.openMenu"));
};

const updateCaseToggleLabels = () => {
  caseCards.forEach((card) => {
    const button = card.querySelector("[data-case-toggle]");
    if (!button) return;

    const isExpanded = card.classList.contains("is-expanded");
    button.textContent = translate(isExpanded ? "cases.readLess" : "cases.readMore");
    button.setAttribute("aria-expanded", String(isExpanded));
  });
};

const setupCaseToggles = () => {
  caseCards.forEach((card, index) => {
    const details = card.querySelector("dl");
    if (!details || card.querySelector("[data-case-toggle]")) return;

    const detailsId = `case-details-${index + 1}`;
    details.id = detailsId;

    const actions = document.createElement("div");
    actions.className = "case-card__actions";

    const button = document.createElement("button");
    button.className = "case-card__toggle";
    button.type = "button";
    button.dataset.caseToggle = "";
    button.setAttribute("aria-controls", detailsId);
    button.setAttribute("aria-expanded", "false");

    button.addEventListener("click", () => {
      card.classList.toggle("is-expanded");
      updateCaseToggleLabels();
    });

    actions.append(button);
    card.append(actions);
  });

  updateCaseToggleLabels();
};

const applyCaseFilter = (filter = "all") => {
  caseFilterButtons.forEach((button) => {
    const isActive = button.dataset.caseFilter === filter;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  caseCards.forEach((card) => {
    const isManualHidden = Boolean(card.dataset.hiddenCase);
    const categories = card.dataset.caseCategories?.split(" ") ?? [];
    const shouldShow = !isManualHidden && (filter === "all" || categories.includes(filter));
    card.hidden = !shouldShow;
  });
};

const applyLanguage = (language) => {
  currentLanguage = supportedLanguages.includes(language) ? language : defaultLanguage;
  document.documentElement.lang = currentLanguage;
  document.title = translate("meta.title");

  const metaDescription = document.querySelector('meta[name="description"]');
  metaDescription?.setAttribute("content", translate("meta.description"));

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = translate(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", translate(element.dataset.i18nAriaLabel));
  });

  const currentCv = cvFiles[currentLanguage] ?? cvFiles[defaultLanguage];
  cvLinks.forEach((link) => {
    link.setAttribute("href", currentCv);
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
    button.setAttribute("aria-label", translate(`language.${button.dataset.language}`));
  });

  updateCaseToggleLabels();
  updateNavToggleLabel();
  updateThemeToggleLabel();
  saveLanguage(currentLanguage);
};

