const showToast = (message) => {
  if (!toast) return;

  toast.textContent = message;
  toast.classList.add("is-visible");
  window.clearTimeout(toastTimeoutId);
  toastTimeoutId = window.setTimeout(() => {
    toast.classList.remove("is-visible");
  }, 2600);
};

const copyText = async (text) => {
  if (navigator.clipboard && window.isSecureContext) {
    await navigator.clipboard.writeText(text);
    return true;
  }

  const textarea = document.createElement("textarea");
  textarea.value = text;
  textarea.setAttribute("readonly", "");
  textarea.style.position = "fixed";
  textarea.style.opacity = "0";
  document.body.appendChild(textarea);
  textarea.select();
  const copied = document.execCommand("copy");
  textarea.remove();
  return copied;
};

emailAction?.addEventListener("click", (event) => {
  event.preventDefault();

  const email = "vcasasus00@gmail.com";
  copyText(email)
    .then((copied) => {
      showToast(`${translate(copied ? "toast.emailCopied" : "toast.email")} ${email}`);
    })
    .catch(() => {
      showToast(`${translate("toast.email")} ${email}`);
    })
    .finally(() => {
      window.setTimeout(() => {
        window.location.href = `mailto:${email}`;
      }, 120);
    });
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  {
    threshold: 0.16,
    rootMargin: "0px 0px -60px",
  }
);

document.querySelectorAll(".reveal").forEach((element, index) => {
  element.style.transitionDelay = `${Math.min(index % 6, 5) * 70}ms`;
  revealObserver.observe(element);
});

const sectionObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      const id = entry.target.getAttribute("id");
      const navLink = id ? document.querySelector(`.nav__link[href="#${id}"]`) : null;

      if (entry.isIntersecting && navLink) {
        document.querySelectorAll(".nav__link").forEach((link) => link.classList.remove("is-active"));
        navLink.classList.add("is-active");
      }
    });
  },
  {
    threshold: 0.34,
    rootMargin: "-20% 0px -55%",
  }
);

document.querySelectorAll("main section[id]").forEach((section) => {
  sectionObserver.observe(section);
});
